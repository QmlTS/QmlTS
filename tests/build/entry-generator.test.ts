import { describe, expect, test } from 'bun:test';
import type { EntryGeneratorOptions, ResolvedPackages } from '../../src/build/build-types.js';
import { createEntryGenerator } from '../../src/build/entry-generator.js';

describe('EntryGenerator', () => {
  const generator = createEntryGenerator();

  function makeOptions(overrides: Partial<EntryGeneratorOptions> = {}): EntryGeneratorOptions {
    return {
      compiledViewModels: [
        { className: 'CounterViewModel', schemaFile: 'schemas/CounterViewModel.schema.json' },
      ],
      mainQml: './qml/CounterView.qml',
      qmlImportPaths: [],
      eventBindingsFile: './event-bindings.json',
      ...overrides,
    };
  }

  // ─── BP-67: Basic entry file generation ──────────────────
  test('BP-67: generates entry file with host creation and QML loading', () => {
    const code = generator.generate(makeOptions());

    expect(code).toContain("import { createHost } from '@qmlts/host'");
    expect(code).toContain('const host = createHost(');
    expect(code).toContain('host.loadQml("./qml/CounterView.qml")');
    expect(code).toContain('host.exec()');
  });

  // ─── BP-68: ViewModel registration ───────────────────────
  test('BP-68: registers ViewModels in entry file', () => {
    const code = generator.generate(makeOptions());

    expect(code).toContain('import { CounterViewModel }');
    expect(code).toContain('host.registerViewModel("CounterViewModel", CounterViewModel)');
  });

  // ─── BP-69: Multiple ViewModels ──────────────────────────
  test('BP-69: handles multiple ViewModels', () => {
    const code = generator.generate(
      makeOptions({
        compiledViewModels: [
          { className: 'CounterViewModel', schemaFile: 'schemas/CounterViewModel.schema.json' },
          { className: 'TodoViewModel', schemaFile: 'schemas/TodoViewModel.schema.json' },
        ],
      }),
    );

    expect(code).toContain('CounterViewModel');
    expect(code).toContain('TodoViewModel');
    expect(code).toContain('host.registerViewModel("CounterViewModel"');
    expect(code).toContain('host.registerViewModel("TodoViewModel"');
  });

  // ─── BP-70: No ViewModels ────────────────────────────────
  test('BP-70: generates valid entry file with no ViewModels', () => {
    const code = generator.generate(makeOptions({ compiledViewModels: [] }));

    expect(code).toContain("import { createHost } from '@qmlts/host'");
    expect(code).toContain('const host = createHost(');
    expect(code).toContain('host.loadQml');
    expect(code).toContain('host.exec()');
    expect(code).not.toContain('registerViewModel');
  });

  // ─── BP-71: Event bindings file ──────────────────────────
  test('BP-71: loads event bindings file', () => {
    const code = generator.generate(makeOptions());

    expect(code).toContain('host.loadEventBindings("./event-bindings.json")');
  });

  // ─── BP-72: QML import paths ─────────────────────────────
  test('BP-72: includes QML import paths in host config', () => {
    const code = generator.generate(
      makeOptions({ qmlImportPaths: ['/opt/qt/qml', '/usr/lib/qml'] }),
    );

    expect(code).toContain('importPaths');
    expect(code).toContain('/opt/qt/qml');
    expect(code).toContain('/usr/lib/qml');
  });

  // ─── BP-73: Package import paths ─────────────────────────
  test('BP-73: merges package import paths into host config', () => {
    const packages: ResolvedPackages = {
      packages: [],
      qmlImportPaths: ['/pkg/qml1', '/pkg/qml2'],
      nativeLibPaths: [],
    };

    const code = generator.generate(makeOptions({ packages }));

    expect(code).toContain('/pkg/qml1');
    expect(code).toContain('/pkg/qml2');
  });

  // ─── BP-74: Deduplicates import paths ────────────────────
  test('BP-74: deduplicates overlapping import paths', () => {
    const packages: ResolvedPackages = {
      packages: [],
      qmlImportPaths: ['/shared/qml'],
      nativeLibPaths: [],
    };

    const code = generator.generate(
      makeOptions({
        qmlImportPaths: ['/shared/qml'],
        packages,
      }),
    );

    const matches = code.match(/\/shared\/qml/g);
    expect(matches).toHaveLength(1);
  });

  // ─── BP-75: Generated comment header ─────────────────────
  test('BP-75: includes generated-file comment', () => {
    const code = generator.generate(makeOptions());

    expect(code).toContain('Generated by QmlTS build system');
    expect(code).toContain('Do not edit manually');
  });

  // ─── BP-76: Backslashes normalized to forward slashes ────
  test('BP-76: normalizes backslashes in paths', () => {
    const code = generator.generate(
      makeOptions({
        mainQml: '.\\qml\\Main.qml',
        eventBindingsFile: '.\\bindings\\events.json',
      }),
    );

    expect(code).not.toContain('\\');
  });
});

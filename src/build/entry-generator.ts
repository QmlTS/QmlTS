import type { EntryGeneratorOptions } from './build-types.js';

// ─── Types ──────────────────────────────────────────────────

export interface EntryGenerator {
  generate(options: EntryGeneratorOptions): string;
}

// ─── Factory ────────────────────────────────────────────────

export function createEntryGenerator(): EntryGenerator {
  return {
    generate(options: EntryGeneratorOptions): string {
      const lines: string[] = [];

      lines.push('// Generated by QmlTS build system');
      lines.push('// Do not edit manually');
      lines.push('');

      // Host import
      lines.push("import { createHost } from '@qmlts/host';");
      lines.push('');

      // ViewModel imports
      if (options.compiledViewModels.length > 0) {
        for (const vm of options.compiledViewModels) {
          lines.push(
            `import { ${vm.className} } from './${vm.schemaFile
              .replace(/\\/g, '/')
              .replace(/\.schema\.json$/, '')}';`,
          );
        }
        lines.push('');
      }

      // Build import paths array
      const importPaths = [...options.qmlImportPaths];
      if (options.packages) {
        for (const p of options.packages.qmlImportPaths) {
          if (!importPaths.includes(p)) {
            importPaths.push(p);
          }
        }
      }

      // Host creation
      lines.push('const host = createHost({');
      if (importPaths.length > 0) {
        lines.push('  importPaths: [');
        for (const p of importPaths) {
          lines.push(`    ${JSON.stringify(p.replace(/\\/g, '/'))},`);
        }
        lines.push('  ],');
      }
      lines.push('});');
      lines.push('');

      // ViewModel registration
      if (options.compiledViewModels.length > 0) {
        for (const vm of options.compiledViewModels) {
          lines.push(`host.registerViewModel(${JSON.stringify(vm.className)}, ${vm.className});`);
        }
        lines.push('');
      }

      // Event bindings
      lines.push(
        `host.loadEventBindings(${JSON.stringify(options.eventBindingsFile.replace(/\\/g, '/'))});`,
      );
      lines.push('');

      // Load main QML
      lines.push(`host.loadQml(${JSON.stringify(options.mainQml.replace(/\\/g, '/'))});`);
      lines.push('');

      // Start event loop
      lines.push('host.exec();');
      lines.push('');

      return lines.join('\n');
    },
  };
}
